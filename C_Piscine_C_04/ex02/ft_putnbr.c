#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	i;
	char	c;

	i = nb;
	if (i < 0)
	{
		write(1, "-", 1);
		i = -i;
	}
	if (i / 10 != 0)
		ft_putnbr(i / 10);
	c = i % 10 + '0';
	write(1, &c, 1);
}
